// dom objects const
const projectDetail = document.querySelector(".project-info");
const projectBtnContainer = document.querySelector(".project-btns");
const proBtns = projectBtnContainer.querySelector("ul");
const nextBtn = document.querySelector(".next");
const prevBtn = document.querySelector(".prev");
// js variable/const
let i = 0;
let firstProject = data[i];
const projectTabBtns = data.map((title) => {
  return `<li class="btn-project" data-id = ${title.index}>${title.company}</li>`;
});
const innerHTMLProBtn = projectTabBtns;

document.addEventListener("DOMContentLoaded", () => {
  projectDetail.innerHTML = innerHtmlProDe;
  proBtns.innerHTML = innerHTMLProBtn;
  prevBtn.style.display = "none";
});
proBtns.addEventListener("click", function selectBtn(e) {
  let index = parseInt(e.target.dataset.id);
  // console.log(index);
  i = index + i;

  toggle();
  i = 0;
});
nextBtn.addEventListener("click", nextProject);
prevBtn.addEventListener("click", prevProject);

// objects inner html

